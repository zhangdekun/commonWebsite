package vo;

public class AreaItem {

	private String x_code;
	private String y_code;
	private String x_name;
	private String y_name;
	private String color;
	public String getX_code() {
		return x_code;
	}
	public void setX_code(String x_code) {
		this.x_code = x_code;
	}
	public String getY_code() {
		return y_code;
	}
	public void setY_code(String y_code) {
		this.y_code = y_code;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getX_name() {
		return x_name;
	}
	public void setX_name(String x_name) {
		this.x_name = x_name;
	}
	public String getY_name() {
		return y_name;
	}
	public void setY_name(String y_name) {
		this.y_name = y_name;
	}
}
